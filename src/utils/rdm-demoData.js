export function resetDemoData() {
  localStorage.removeItem('rdm_initialized')
  // Clear all keys
  const keys = ['rdm_projects','rdm_tasks','rdm_documents','rdm_ai_tasks','rdm_activities',
    'rdm_test_records','rdm_risks','rdm_ecn','rdm_summaries','rdm_gate_reviews']
  keys.forEach(k => localStorage.removeItem(k))
  initDemoData()
}
